    private void jToggleButtonSwitchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonSwitchActionPerformed
        if (ejecucion == false) {
            ejecucion = true;
            hilo = new Thread(this);
            this.hilo.start();
            jToggleButtonSwitch.setText("OFF");
        } else {
            jToggleButtonSwitch.setText("ON");
            ejecucion = false;;
            jLIndicadorIncendio.setForeground(Color.BLACK);
            jLIndicadorIntrusos.setForeground(Color.BLACK);
            jLIndicadorLuces.setForeground(Color.BLACK);
            jLIndicadorHumo.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_jToggleButtonSwitchActionPerformed


