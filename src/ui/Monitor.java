    @Override
    public void run() {
        while (ejecucion) {
            try {
                if (datos.humo == 0) {
                    jLIndicadorHumo.setForeground(Color.RED);
                } else {
                    jLIndicadorHumo.setForeground(Color.BLACK);
                }
                if (datos.luces == 1) {
                    jLIndicadorLuces.setForeground(Color.GREEN);
                } else {
                    jLIndicadorLuces.setForeground(Color.BLACK);
                }
                if (datos.intruso == 1) {
                    jLIndicadorIntrusos.setForeground(Color.red);
                } else {
                    jLIndicadorIntrusos.setForeground(Color.BLACK);
                }
                if (datos.fuego == 0) {
                    jLIndicadorIncendio.setForeground(Color.red);
                } else {
                    jLIndicadorIncendio.setForeground(Color.BLACK);
                }
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(Monitor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
